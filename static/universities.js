// 搜索匹配函数
function searchUniversities(query) {
  var q = query.trim().toLowerCase();
  if (!q) return [];
  return UNIVERSITIES.filter(function(u) {
    return u.n.toLowerCase().indexOf(q) >= 0 || u.c.indexOf(query) >= 0;
  }).slice(0, 8);
}

// 获取学校标签
function getUniversityTags(name) {
  var found = UNIVERSITIES.find(function(u) { return u.n === name; });
  return found ? found.t : [];
}

// 格式化标签为显示文本
function formatTags(tags) {
  if (!tags.length) return "";
  return tags.map(function(t) {
    if (t === "985") return '<span class="uni-tag tag-985">985</span>';
    if (t === "211") return '<span class="uni-tag tag-211">211</span>';
    if (t === "双一流") return '<span class="uni-tag tag-dyl">双一流</span>';
    return "";
  }).join(" ");
}
